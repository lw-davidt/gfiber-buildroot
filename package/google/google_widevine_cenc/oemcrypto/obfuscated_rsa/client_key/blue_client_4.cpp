  BN_CTX* bn_ctx = BN_CTX_new();
  BN_CTX_init(bn_ctx);

#define X(m, r, g) \
    BN_mul(r, m, r, bn_ctx);\
    BN_mod(r, r, g, bn_ctx)

#define Y(m, r, g) \
    BN_mul(m, m, m, bn_ctx);\
    BN_mod(m, m, g, bn_ctx)

#define SWAP(i, j) \
    { \
      BIGNUM tmp; \
      tmp = m[i]; m[i] = m[j]; m[j] = tmp; \
      tmp = r[i]; r[i] = r[j]; r[j] = tmp; \
    }

    /* real_j is 2, bit 209 is 1 */
    X(&m[0], &r[0], g);
    Y(&m[1], &r[1], g);
    X(&m[2], &r[2], g);
    Y(&m[0], &r[0], g);
    Y(&m[1], &r[1], g);
    Y(&m[2], &r[2], g);
    /* real_j is 2, bit 210 is 1 */
    X(&m[0], &r[0], g);
    X(&m[1], &r[1], g);
    X(&m[2], &r[2], g);
    X(&m[0], &r[0], g);
    X(&m[1], &r[1], g);
    Y(&m[2], &r[2], g);
    SWAP(0, 0);
    SWAP(1, 0);
    SWAP(2, 0);
    /* real_j is 0, bit 211 is 1 */
